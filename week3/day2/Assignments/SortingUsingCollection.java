public class SortingUsingCollection {

	public static void main(String[] args) {
		String[] company = {"Google","Microsoft","Testleaf","Maverick"};
		int length= company.length;
		for (int i = length-1; i >=0; i--) {
			System.out.println(company[i]);
		}

	}

}
